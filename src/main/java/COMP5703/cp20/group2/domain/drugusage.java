package COMP5703.cp20.group2.domain;

public class drugusage {

	private String patientID;
	private Double Aspirin;
	private Double Warfarin;
	private Double clopidogrel;
	
	
	public String getPatientID() {
		return patientID;
	}
	public void setPatientID(String patientID) {
		this.patientID = patientID;
	}
	public Double getAspirin() {
		return Aspirin;
	}
	public void setAspirin(Double aspirin) {
		Aspirin = aspirin;
	}
	public Double getWarfarin() {
		return Warfarin;
	}
	public void setWarfarin(Double warfarin) {
		Warfarin = warfarin;
	}
	public Double getClopidogrel() {
		return clopidogrel;
	}
	public void setClopidogrel(Double clopidogrel) {
		this.clopidogrel = clopidogrel;
	}
	
	
	
	
}
